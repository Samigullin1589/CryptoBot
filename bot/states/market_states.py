from aiogram.fsm.state import State, StatesGroup

class MarketStates(StatesGroup):
    """
    Определяет шаги конечного автомата для процессов на рынке.
    """
    # Состояния для процесса выставления лота на продажу
    choosing_asic_to_sell = State()  # Шаг 1: Пользователь выбирает ASIC из своего ангара
    entering_price = State()         # Шаг 2: Пользователь вводит цену для выбранного ASIC