from aiogram.fsm.state import State, StatesGroup

class AsicExplorerStates(StatesGroup):
    """
    Состояния для сценариев взаимодействия с информацией об ASIC.
    """
    viewing_top_list = State()  # Пользователь просматривает список топ-ASIC
    viewing_passport = State()  # Пользователь просматривает паспорт конкретного ASIC
    setting_cost = State()      # Пользователь находится в меню выбора тарифа
