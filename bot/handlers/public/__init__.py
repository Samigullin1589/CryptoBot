from aiogram import Router

# Создаем единый роутер для публичной части для удобства экспорта,
# но НЕ включаем в него другие роутеры здесь.
# Это делается для того, чтобы `main.py` мог импортировать один объект.
# В качестве альтернативы, `main.py` мог бы импортировать каждый роутер
# по отдельности, но этот подход сохраняет некоторую структуру.
public_router = Router(name="public_handlers")

